class Muebles:
	def __init__(self, nombre, material, uso, piezas):
		self.nombre= nombre
		self.material=material
		self.uso=uso
		self.piezas=piezas
	def atributos():
		return f'El/la {self.nombre}, esta hecho de {self.material}, tiene la finalidad de {self.uso} y cosnta de {self.piezas} pieza(s)'



